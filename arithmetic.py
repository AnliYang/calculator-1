def add(num1, num2):
    """Returns the sum of the two input integers."""

    return num1 + num2

def subtract(num1, num2):
    """Returns the difference of the two input integers."""

    return num1 - num2    

def multiply(num1, num2):
    """Returns the product of the two input integers."""

    return num1 * num2    

def divide(num1, num2):
    """Returns the quotient of the two input integers."""

    return float(num1) / num2    

def square(num1):
    """Returns the square of the input integer."""

    return num1 ** 2
    
def cube(num1):
    """Returns the cube of the input integer."""

    return num1 ** 3

def power(num1, num2):
    """Returns the result of the first integer raised to the power of the second."""

    return num1 ** num2    

def mod(num1, num2):
    """Returns the remainder of the first integer divided by the second integer."""

    return num1 % num2    
